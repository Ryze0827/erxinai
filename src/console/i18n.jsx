function interpolate(value, variables = {}) {
  return Object.entries(variables).reduce(
    (result, [key, replacement]) => result.replaceAll(`{${key}}`, String(replacement)),
    value,
  );
}

const LocaleContext = createContext(null);

export function LocaleProvider({ children }) {
  const [locale, setLocaleState] = useState(() => localStorage.getItem(STORAGE_KEY) === "zh" ? "zh" : "en");
  const setLocale = (value) => setLocaleState(value === "zh" ? "zh" : "en");

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, locale);
    document.documentElement.lang = locale === "zh" ? "zh-CN" : "en";
  }, [locale]);

  const value = useMemo(() => {
    const t = (key, variables) => interpolate(dictionaries[locale]?.[key] || dictionaries.en[key] || key, variables);
    const formatNumber = (number, options) => new Intl.NumberFormat(locale === "zh" ? "zh-CN" : "en-US", options).format(Number(number) || 0);
    const formatCurrency = (number, currency = "USD") => new Intl.NumberFormat(locale === "zh" ? "zh-CN" : "en-US", {
      style: "currency",
      currency,
      maximumFractionDigits: currency === "USD" ? 4 : 2,
    }).format(Number(number) || 0);
    const formatDate = (date, options = {}) => {
      if (!date) return "—";
      const parsed = new Date(date);
      if (Number.isNaN(parsed.getTime())) return "—";
      return new Intl.DateTimeFormat(locale === "zh" ? "zh-CN" : "en-US", {
        dateStyle: "medium",
        timeStyle: options.dateOnly ? undefined : "short",
      }).format(parsed);
    };
    return { locale, setLocale, t, formatNumber, formatCurrency, formatDate };
  }, [locale]);

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  return useContext(LocaleContext);
}
