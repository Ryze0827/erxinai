const supportedLanguages = [
  { code: "zh", label: "中文", htmlLang: "zh-CN" },
  { code: "zh-Hant", label: "繁體中文", htmlLang: "zh-Hant" },
  { code: "en", label: "English", htmlLang: "en" },
];

const header = document.querySelector(".site-header");
const navToggle = document.querySelector(".nav-toggle");
const navDrawer = document.querySelector(".nav-drawer");
const drawerLinks = document.querySelectorAll(".nav-drawer a");
const tabs = document.querySelectorAll(".tab");
const panels = document.querySelectorAll(".tab-panel");
const faqItems = document.querySelectorAll(".faq-item");
const languagePickers = document.querySelectorAll("[data-language-picker]");
const metaDescription = document.querySelector('meta[name="description"]');
const savedLanguage = localStorage.getItem("erxinai-language");
let currentLanguage = supportedLanguages.some((item) => item.code === savedLanguage) ? savedLanguage : "zh";

function text(key) {
  return translations[currentLanguage][key] || translations.zh[key] || "";
}

function renderLanguageOptions() {
  languagePickers.forEach((picker) => {
    const menu = picker.querySelector("[data-language-menu]");
    menu.innerHTML = "";
    supportedLanguages.forEach((language) => {
      if (!translations[language.code]) return;
      const option = document.createElement("button");
      option.type = "button";
      option.className = "language-option";
      option.dataset.languageOption = language.code;
      option.setAttribute("role", "option");
      option.textContent = language.label;
      menu.appendChild(option);
    });
  });
}

function syncHeader() {
  header.dataset.scrolled = window.scrollY > 8 ? "true" : "false";
}

function setNavToggleLabel(open) {
  navToggle.setAttribute("aria-label", text(open ? "nav.toggleClose" : "nav.toggleLabel"));
}

function setDrawer(open) {
  navDrawer.dataset.open = String(open);
  navToggle.setAttribute("aria-expanded", String(open));
  document.body.classList.toggle("drawer-open", open);
  setNavToggleLabel(open);
}

function setLanguageMenu(picker, open) {
  picker.dataset.open = String(open);
  picker.querySelector("[data-language-button]").setAttribute("aria-expanded", String(open));
}

function closeLanguageMenus(exceptPicker) {
  languagePickers.forEach((picker) => {
    if (picker !== exceptPicker) setLanguageMenu(picker, false);
  });
}

function selectTab(index) {
  tabs.forEach((tab, tabIndex) => {
    const active = tabIndex === index;
    tab.classList.toggle("active", active);
    tab.setAttribute("aria-selected", String(active));
  });
  panels.forEach((panel, panelIndex) => {
    panel.classList.toggle("active", panelIndex === index);
  });
}

function toggleFaq(item) {
  const isOpen = item.dataset.open === "true";
  const button = item.querySelector(".faq-q");
  item.dataset.open = String(!isOpen);
  button.setAttribute("aria-expanded", String(!isOpen));
}

function applyLanguage(language) {
  currentLanguage = language;
  const languageMeta = supportedLanguages.find((item) => item.code === language) || supportedLanguages[0];
  document.documentElement.lang = languageMeta.htmlLang;
  document.title = text("page.title");
  metaDescription.setAttribute("content", text("page.description"));
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = text(node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    node.setAttribute("aria-label", text(node.dataset.i18nAriaLabel));
  });
  languagePickers.forEach((picker) => {
    const current = picker.querySelector("[data-language-current]");
    const button = picker.querySelector("[data-language-button]");
    current.textContent = languageMeta.label;
    button.setAttribute("aria-label", text("language.label"));
    button.setAttribute("title", text("language.label"));
    picker.querySelectorAll("[data-language-option]").forEach((option) => {
      const active = option.dataset.languageOption === language;
      option.dataset.active = String(active);
      option.setAttribute("aria-selected", String(active));
    });
  });
  closeLanguageMenus();
  setNavToggleLabel(navDrawer.dataset.open === "true");
  localStorage.setItem("erxinai-language", language);
}

function handleLanguageOptionClick(event) {
  const option = event.target.closest("[data-language-option]");
  if (option && translations[option.dataset.languageOption]) {
    applyLanguage(option.dataset.languageOption);
  }
}

function handleLanguageButtonClick(event) {
  const picker = event.currentTarget.closest("[data-language-picker]");
  const nextOpen = picker.dataset.open !== "true";
  closeLanguageMenus(picker);
  setLanguageMenu(picker, nextOpen);
}

window.addEventListener("scroll", syncHeader, { passive: true });
syncHeader();
renderLanguageOptions();
applyLanguage(currentLanguage);

navToggle.addEventListener("click", () => {
  setDrawer(navDrawer.dataset.open !== "true");
});

drawerLinks.forEach((link) => {
  link.addEventListener("click", () => setDrawer(false));
});

tabs.forEach((tab) => {
  tab.addEventListener("click", () => selectTab(Number(tab.dataset.tab)));
});

faqItems.forEach((item) => {
  item.querySelector(".faq-q").addEventListener("click", () => toggleFaq(item));
});

languagePickers.forEach((picker) => {
  picker.querySelector("[data-language-button]").addEventListener("click", handleLanguageButtonClick);
  picker.querySelector("[data-language-menu]").addEventListener("click", handleLanguageOptionClick);
});

document.addEventListener("click", (event) => {
  if (!event.target.closest("[data-language-picker]")) {
    closeLanguageMenus();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeLanguageMenus();
    setDrawer(false);
  }
});
